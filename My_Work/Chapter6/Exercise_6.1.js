// We are defining a function called 'adder' that takes two parameters, 'a' and 'b'.
function adder(a, b) {
    // The function adds 'a' and 'b' together and returns the result.
    return a + b;
}

// We have two variables 'val1' and 'val2' with specific numbers.
const val1 = 10; 
const val2 = 20;

// We are calling the 'adder' function with 'val1' and 'val2' as arguments and showing the result in the console.
console.log(adder(val1, val2));

// We are calling the 'adder' function again with different numbers (20 and 30) and displaying the result in the console.
console.log(adder(20, 30));
