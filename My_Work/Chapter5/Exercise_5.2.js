// Initialize a counter variable to keep track of the current value
let counter = 0;
// Define the step size to increment the counter by
let step = 5;

// Execute the loop at least once, and continue as long as the counter is less than or equal to 100
do {
    // Output the current value of the counter
    console.log(counter);
    // Increment the counter by the defined step size
    counter += step;
} while (counter <= 100); 
